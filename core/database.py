def init_db() -> None:
    """Create all database tables if they do not already exist.

    Safe to call multiple times (uses ``CREATE TABLE IF NOT EXISTS``).
    Creates five tables: ``sessions``, ``research``, ``questions``,
    ``answers``, and ``reports``.  See module docstring for the full schema.

    Returns:
        None

    Raises:
        sqlite3.Error: If the database file cannot be opened or the DDL
            statements fail for any reason.
    """
    conn = _get_connection()
    with conn:
        conn.execute("""
            CREATE TABLE IF NOT EXISTS sessions (
                session_id    TEXT PRIMARY KEY,
                company       TEXT NOT NULL,
                role          TEXT NOT NULL,
                level         TEXT NOT NULL,
                state         TEXT NOT NULL,
                num_questions INTEGER NOT NULL DEFAULT 5,
                created_at    TEXT NOT NULL
            )
        """)
        conn.execute("""
            CREATE TABLE IF NOT EXISTS research (
                session_id  TEXT PRIMARY KEY,
                data        TEXT NOT NULL
            )
        """)
        conn.execute("""
            CREATE TABLE IF NOT EXISTS questions (
                session_id  TEXT NOT NULL,
                q_index     INTEGER NOT NULL,
                data        TEXT NOT NULL,
                PRIMARY KEY (session_id, q_index)
            )
        """)
        conn.execute("""
            CREATE TABLE IF NOT EXISTS answers (
                session_id   TEXT NOT NULL,
                q_index      INTEGER NOT NULL,
                answer_text  TEXT NOT NULL,
                evaluation   TEXT NOT NULL,
                answered_at  TEXT NOT NULL,
                PRIMARY KEY (session_id, q_index)
            )
        """)
        conn.execute("""
            CREATE TABLE IF NOT EXISTS reports (
                session_id  TEXT PRIMARY KEY,
                data        TEXT NOT NULL,
                created_at  TEXT NOT NULL
            )
        """)
        conn.execute("""
            CREATE TABLE IF NOT EXISTS follow_up_tracking (
                session_id  TEXT NOT NULL,
                q_index     INTEGER NOT NULL,
                count       INTEGER NOT NULL DEFAULT 0,
                PRIMARY KEY (session_id, q_index)
            )
        """)
        # Migration: add category column to answers table for existing DBs
        try:
            conn.execute("ALTER TABLE answers ADD COLUMN category TEXT NOT NULL DEFAULT ''")
        except Exception:
            pass  # column already exists on fresh DB, safe to ignore
    conn.close()


def create_session(
    session_id: str,
    company: str,
    role: str,
    level: str,
    num_questions: int = 5,
) -> None:
    """Insert a new session row into the ``sessions`` table.

    The initial state is always ``STATE_SETUP`` (imported from config).
    ``created_at`` is set to the current UTC time.

    Args:
        session_id: UUID string generated by the orchestrator for this session.
        company: Company name as entered by the user.
        role: Job role as entered by the user.
        level: Experience level as entered by the user (e.g. "junior", "senior").
        num_questions: Number of questions for this session (2–15).

    Returns:
        None

    Raises:
        sqlite3.IntegrityError: If a session with the same ``session_id``
            already exists (PRIMARY KEY violation).
        sqlite3.Error: On any other database error.
    """
    from core.config import STATE_SETUP  # local import avoids circular dependency at module level
    conn = _get_connection()
    with conn:
        conn.execute(
            """
            INSERT INTO sessions (session_id, company, role, level, state, num_questions, created_at)
            VALUES (?, ?, ?, ?, ?, ?, ?)
            """,
            (session_id, company, role, level, STATE_SETUP, num_questions, _now_iso()),
        )
    conn.close()


def get_session(session_id: str) -> dict | None:
    """Fetch a session row by its ``session_id``.

    Args:
        session_id: UUID string identifying the session to retrieve.

    Returns:
        A dict with keys ``session_id``, ``company``, ``role``, ``level``,
        ``state``, ``created_at`` if the row exists, or ``None`` if no
        matching row is found.

    Raises:
        sqlite3.Error: On any database error.
    """
    conn = _get_connection()
    conn.row_factory = sqlite3.Row
    cursor = conn.execute(
        "SELECT session_id, company, role, level, state, num_questions, created_at "
        "FROM sessions WHERE session_id = ?",
        (session_id,),
    )
    row = cursor.fetchone()
    conn.close()
    if row is None:
        return None
    return dict(row)


def save_research(session_id: str, research_dict: dict) -> None:
    """Persist the Researcher agent's output for a session.

    Uses ``INSERT OR REPLACE`` so calling this function a second time for
    the same session overwrites the previous research data (idempotent).

    The ``research_dict`` must conform to the 8-key Researcher contract
    defined in anti-hallucination.md:
    ``{company, role, interview_rounds, key_topics, difficulty,
    culture_keywords, known_question_types, red_flags_to_test}``

    Args:
        session_id: UUID string identifying the owning session.
        research_dict: Validated Researcher output dict with exactly 8 keys.

    Returns:
        None

    Raises:
        ValueError: If ``research_dict`` is missing any of the 8 required keys.
        sqlite3.Error: On any database error.
    """
    required = {
        "company", "role", "interview_rounds", "key_topics",
        "difficulty", "culture_keywords", "known_question_types", "red_flags_to_test",
    }
    missing = required - set(research_dict.keys())
    if missing:
        raise ValueError(f"save_research: missing keys in research_dict: {missing}")

    conn = _get_connection()
    with conn:
        conn.execute(
            "INSERT OR REPLACE INTO research (session_id, data) VALUES (?, ?)",
            (session_id, json.dumps(research_dict)),
        )
    conn.close()


def save_questions(session_id: str, questions_list: list[dict]) -> None:
    """Persist all generated questions for a session.

    Each element of ``questions_list`` is one Question_Dict entry from the
    QuestionGenerator output (keys: ``id, category, question, ideal_keywords,
    difficulty, follow_ups, scoring_hint``).

    Rows are inserted with their list position as ``q_index`` (0-based).
    Uses ``INSERT OR REPLACE`` per row so the call is idempotent.

    Args:
        session_id: UUID string identifying the owning session.
        questions_list: List of question dicts (2–15 items),
            each conforming to the QuestionGenerator 7-key contract.

    Returns:
        None

    Raises:
        ValueError: If ``questions_list`` is empty, or if any item is missing required keys.
        sqlite3.Error: On any database error.
    """
    from core.config import MIN_QUESTIONS, MAX_QUESTIONS  # local import; avoids potential circular refs
    if not questions_list or len(questions_list) < MIN_QUESTIONS or len(questions_list) > MAX_QUESTIONS:
        raise ValueError(
            f"save_questions: expected {MIN_QUESTIONS}-{MAX_QUESTIONS} questions, got {len(questions_list)}"
        )
    required = {"id", "category", "question", "ideal_keywords", "difficulty", "follow_ups", "scoring_hint"}
    for i, q in enumerate(questions_list):
        missing = required - set(q.keys())
        if missing:
            raise ValueError(f"save_questions: question[{i}] missing keys: {missing}")

    conn = _get_connection()
    with conn:
        for q_index, q in enumerate(questions_list):
            conn.execute(
                """
                INSERT OR REPLACE INTO questions (session_id, q_index, data)
                VALUES (?, ?, ?)
                """,
                (session_id, q_index, json.dumps(q)),
            )
    conn.close()


def save_answer(
    session_id: str,
    q_index: int,
    answer_text: str,
    evaluation_dict: dict,
    category: str = "",
) -> None:
    """Persist a user's answer and its evaluated result for one question.

    Uses ``INSERT OR REPLACE`` so re-evaluating an answer (e.g. after a
    follow-up) replaces the previous row for the same ``(session_id, q_index)``.

    The ``evaluation_dict`` must conform to the 6-key Evaluator contract
    defined in anti-hallucination.md:
    ``{scores, total, verdict, feedback, missing_keywords, trigger_follow_up}``

    Args:
        session_id: UUID string identifying the owning session.
        q_index: Zero-based index of the question being answered (0–9).
        answer_text: The raw answer text submitted by the user.
        evaluation_dict: Validated Evaluator output dict with exactly 6 keys.
        category: The question category (e.g. "technical", "behavioral").

    Returns:
        None

    Raises:
        ValueError: If ``evaluation_dict`` is missing any of the 6 required keys.
        sqlite3.Error: On any database error.
    """
    required = {"scores", "total", "verdict", "feedback", "missing_keywords", "trigger_follow_up"}
    missing = required - set(evaluation_dict.keys())
    if missing:
        raise ValueError(f"save_answer: missing keys in evaluation_dict: {missing}")

    conn = _get_connection()
    with conn:
        conn.execute(
            """
            INSERT OR REPLACE INTO answers
                (session_id, q_index, answer_text, evaluation, category, answered_at)
            VALUES (?, ?, ?, ?, ?, ?)
            """,
            (
                session_id,
                q_index,
                answer_text,
                json.dumps(evaluation_dict),
                category,
                _now_iso(),
            ),
        )
    conn.close()


def get_answers(session_id: str) -> list[dict]:
    """Retrieve all stored answers for a session, ordered by question index.

    Called by the Coach agent to build the compressed answer summary needed
    for the final report prompt.

    Args:
        session_id: UUID string identifying the owning session.

    Returns:
        A list of dicts ordered by ``q_index`` ascending.  Each dict has keys:
        ``q_index`` (int), ``answer_text`` (str), ``evaluation`` (dict —
        the deserialised Evaluation_Dict), ``answered_at`` (str ISO-8601).
        Returns an empty list if no answers exist for the session.

    Raises:
        sqlite3.Error: On any database error.
    """
    conn = _get_connection()
    conn.row_factory = sqlite3.Row
    cursor = conn.execute(
        "SELECT q_index, answer_text, evaluation, category, answered_at "
        "FROM answers WHERE session_id = ? ORDER BY q_index ASC",
        (session_id,),
    )
    rows = cursor.fetchall()
    conn.close()
    result = []
    for row in rows:
        result.append({
            "q_index": row["q_index"],
            "answer_text": row["answer_text"],
            "evaluation": json.loads(row["evaluation"]),
            "category": row["category"],
            "answered_at": row["answered_at"],
        })
    return result


