import json
import sqlite3
from datetime import datetime, timezone

from core.config import DB_PATH

# ---------------------------------------------------------------------------
# Private helper
# ---------------------------------------------------------------------------


def _get_connection() -> sqlite3.Connection:
    """Open and return a new SQLite connection to DB_PATH.

    The connection is configured with:
    - ``check_same_thread=False`` so that Streamlit (which may access the
      connection from multiple threads) does not raise an error.
    - ``isolation_level=None`` (autocommit) is NOT set here; callers use the
      context-manager form ``with conn:`` to commit/rollback atomically.

    Returns:
        A ``sqlite3.Connection`` instance ready for use.
    """
    return sqlite3.connect(DB_PATH, check_same_thread=False)


def _now_iso() -> str:
    """Return the current UTC time as an ISO-8601 string.

    Format: ``YYYY-MM-DDTHH:MM:SS.ffffff+00:00``

    Returns:
        Current UTC datetime formatted as an ISO-8601 string with timezone info.
    """
    return datetime.now(timezone.utc).isoformat()


